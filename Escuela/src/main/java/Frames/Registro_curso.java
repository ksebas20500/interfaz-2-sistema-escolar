    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
           if (txtCurso.getText().isEmpty() || txtCodigo.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "No puedes dejar campos vacíos");
        } else {
            // Añadir datos a la lista local
            String[] nuevoCurso = {txtCodigo.getText(), txtCurso.getText()};
            datos.add(nuevoCurso);

            // Actualizar la tabla
            modelo.addRow(nuevoCurso);
            
            // Limpiar campos
            limpiar();
        }
                                        

    }//GEN-LAST:event_btnGuardarActionPerformed

