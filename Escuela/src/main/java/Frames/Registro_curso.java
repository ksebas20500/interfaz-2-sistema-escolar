    void limpiar() {
        txtCurso.setText("");
        txtCodigo.setText("");
    }

    void mostrartabla() {
        modelo = new DefaultTableModel();
        modelo.addColumn("Id");
        modelo.addColumn("Curso");

        tabla_registro_cursos.setModel(modelo);

         
        for (String[] fila : datos) {
            modelo.addRow(fila);
        }
    }
    private void actualizarRegistro() {
     
    int filaSeleccionada = tabla_registro_cursos.getSelectedRow();

  
    if (filaSeleccionada != -1) {
         
        String curso = txtCurso.getText();
        String codigo = txtCodigo.getText();

         
        String[] datosActualizados = {String.valueOf(filaSeleccionada + 1), curso};
        datos.set(filaSeleccionada, datosActualizados);

         
        modelo.setValueAt(curso, filaSeleccionada, 1);

        
        limpiar();
    } else {
        JOptionPane.showMessageDialog(this, "Por favor, seleccione una fila para actualizar");
    }
}

